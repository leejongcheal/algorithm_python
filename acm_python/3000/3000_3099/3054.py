s=input()
i=1
a1,a2,a3 = "","",""
for c in s:
    if i%3==1 and i//3>0:
        a1=a1+".#.."
        a2=a2+"#.#."
        a3=a3+".{}.#".format(c)
    elif i%3!=0:
        a1=a1[0:-1]+"..#.."
        a2=a2[0:-1]+".#.#."
        a3=a3[0:-1]+"#.{}.#".format(c)
    elif i%3==0:
        a1=a1[0:-1]+"..*.."
        a2=a2[0:-1]+ ".*.*."
        a3=a3[0:-1]+"*.{}.*".format(c)
    i+=1
print(a1,a2,a3,a2,a1,sep="\n")